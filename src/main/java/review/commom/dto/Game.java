package review.commom.dto;

public class Game {
	
	public int gid, gtype, gdate, gscore;
	public String gname, genre, gpublic;
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public int getGtype() {
		return gtype;
	}
	public void setGtype(int gtype) {
		this.gtype = gtype;
	}
	public int getGdate() {
		return gdate;
	}
	public void setGdate(int gdate) {
		this.gdate = gdate;
	}
	public int getGscore() {
		return gscore;
	}
	public void setGscore(int gscore) {
		this.gscore = gscore;
	}
	public String getGname() {
		return gname;
	}
	public void setGname(String gname) {
		this.gname = gname;
	}
	public String getGenre() {
		return genre;
	}
	public void setGenre(String genre) {
		this.genre = genre;
	}
	public String getGpublic() {
		return gpublic;
	}
	public void setGpublic(String gpublic) {
		this.gpublic = gpublic;
	}
	
	
	

}
