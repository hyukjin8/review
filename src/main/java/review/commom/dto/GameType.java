package review.commom.dto;

public class GameType {

	public int gtype, gnew, ghot, gbeta;

	public int getGtype() {
		return gtype;
	}

	public void setGtype(int gtype) {
		this.gtype = gtype;
	}

	public int getGnew() {
		return gnew;
	}

	public void setGnew(int gnew) {
		this.gnew = gnew;
	}

	public int getGhot() {
		return ghot;
	}

	public void setGhot(int ghot) {
		this.ghot = ghot;
	}

	public int getGbeta() {
		return gbeta;
	}

	public void setGbeta(int gbeta) {
		this.gbeta = gbeta;
	}
	
	
	
}
